package com.aerolinea.amonic.Dto;

public class FiltrarPrecio {
    private Integer tickets;
    private Integer silla;
    private Integer vuelo;


    public Integer getTickets() {
        return tickets;
    }

    public void setTickets(Integer tickets) {
        this.tickets = tickets;
    }

    public Integer getSilla() {
        return silla;
    }

    public void setSilla(Integer silla) {
        this.silla = silla;
    }

    public Integer getVuelo() {
        return vuelo;
    }

    public void setVuelo(Integer vuelo) {
        this.vuelo = vuelo;
    }
}
